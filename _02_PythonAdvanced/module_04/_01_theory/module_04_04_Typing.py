from typing import List


def find_max(numbers: List[int]) -> int:
    """
    Функция находит максимальное значение в списке
    :param numbers: List[int] - список целых чисел
    :return: max(numbers) - максимальное число из списка
    """
    return max(numbers)


def find_min(numbers: list[int]) -> int:
    """
    Функция находит минимальное значение в списке
    :param numbers: List[int] - список целых чисел
    :return: min(numbers) - минимальное число из списка
    """
    return min(numbers)

